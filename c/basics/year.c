#include <stdio.h>

int main(){
    int year; // Declares the integer variable "year"

    printf("Enter a year: \n"); // Outputs what is in the parenthesis, and makes a new line with \n.
    scanf("%d", &year); // Asks for input, which changes the variable year.
    printf("The year is %d", year); // Outputs the year
    return 0;
}